class Gallery extends Component {
  state = {
    eachObjectImage: imagesList[0].id,
  }

  onEachThumbnailClick = id => {
    this.setState({eachObjectImage: id})
  }

  render() {
    const {eachObjectImage} = this.state
    const {imageAltText, imageUrl} = imagesList[eachObjectImage]
    return (
      <div className="bg-container">
        <div className="container-images">
          <img src={imageUrl} alt={imageAltText} className="image-Url" />
          <h1 className="heading-Photography">Nature Photography</h1>
          <p className="description-Photography">Nature Photography By Rahul</p>
          <ul className="list-images">
            {imagesList.map(eachImage => (
              <ThumbnailsItem
                onEachThumbnailClick={this.onEachThumbnailClick}
                eachImageList={eachImage}
                key={eachImage.id}
                isActive={eachImage.id === eachObjectImage.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

